.ORIG X3000

START

LD R0, VAR_X
LD R1, VAR_Y

ST R0, RESULT_ADDR

;2'S compliment
NOT R1, R1
ADD R1, R1, #1

ADD R0, R0, R1

BRz IF_ZERO
BRnzp ELSE


IF_ZERO
AND R3, R3, #0
ADD R3, R3, #5
BRnzp END_IF


ELSE
AND R3, R3, #0
ADD R3, R3, #5
NOT R3, R3
ADD R3, R3, #1



END_IF

HALT


VAR_X .FILL #2
VAR_Y .FILL #3
RESULT_ADDR .FILL X8002

.END